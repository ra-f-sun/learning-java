
public class OneDArray {
    public static void main(String[] args) {
        int array[] = { 10, 65, 444, 2, 336, 31, 7, 555, 69, 3 };

        System.out.println("The elements of the array are: ");
        for (int i = 0; i < array.length; i++) {
            System.out.print(array[i] + " ");
        }
    }
}
