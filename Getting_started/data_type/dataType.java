//In this file, we will apply the primitive data types used in java

public class DataType {
    public static void main(String[] args) {
        // integer type data
        byte a = 15;
        short b = 30;
        int c = 500;
        long d = 1545454654;

        // float type data
        float e = 66.37f;
        double f = 4545575454.35545;

        // character type
        char g = 'R';

        // boolean type
        boolean h = true;

        // Printing all of them
        System.out.println(a + " " + b + " " + c + " " + d + " " + e + " " + f + " " + g + " " + h);

    }
}